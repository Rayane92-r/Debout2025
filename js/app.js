function showChapter(i) {
    const ch = chapters[i];
    if (!ch) return;

    document.getElementById("chapter-title").textContent = ch.title;

    document.getElementById("chapter-content").innerHTML = ch.content
        .trim()
        .split("\n\n")
        .map(p => `<p>${p.trim()}</p>`)
        .join("");

    updateButtons();
    updateProgress();
    fadeEffect();
}

// =======================
//   BOUTONS
// =======================
function updateButtons() {
    document.getElementById("prev").disabled = current === 0;
    document.getElementById("next").disabled = current === chapters.length - 1;
}

// =======================
//   BARRE DE PROGRESSION
// =======================
function updateProgress() {
    let total = chapters.length;
    let percent = ((current + 1) / total) * 100;

    document.getElementById("progress-fill").style.width = percent + "%";
    document.getElementById("progress-text").textContent = `Chapitre ${current + 1} / ${total}`;
}

// =======================
//   ANIMATION FADE
// =======================
function fadeEffect() {
    const container = document.querySelector(".reader-container");
    container.classList.remove("fade");
    setTimeout(() => container.classList.add("fade"), 10);
}

// =======================
//   NAVIGATION
// =======================
document.getElementById("prev").addEventListener("click", () => {
    if (current > 0) {
        current--;
        showChapter(current);
    }
});

document.getElementById("next").addEventListener("click", () => {
    if (current < chapters.length - 1) {
        current++;
        showChapter(current);
    }
});

// =======================
//   AU LANCEMENT
// =======================
showChapter(0);
